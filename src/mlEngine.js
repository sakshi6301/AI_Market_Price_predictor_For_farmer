function seasonClimate(season) {
  if (season === "Kharif") return { temperature: 1, humidity: 8, rainfall: 28 };
  if (season === "Rabi") return { temperature: -4, humidity: -8, rainfall: -18 };
  return { temperature: 4, humidity: -2, rainfall: -8 };
}

export function getCityWeather({ state, region, season }) {
  const base = stateClimate[state] ?? { temperature: 29, humidity: 62, rainfall: 44, condition: "Mixed farm weather" };
  const seasonal = seasonClimate(season);
  const hash = textHash(`${state}-${region}`);
  const coastalBoost = ["Mumbai", "Kochi", "Chennai", "Kolkata", "Visakhapatnam", "Thiruvananthapuram", "Puri"].includes(region) ? 9 : 0;
  const plateauDryness = ["Pune", "Nashik", "Nagpur", "Bhopal", "Indore", "Jaipur"].includes(region) ? -8 : 0;
  const rainfall = Math.round(clamp(base.rainfall + seasonal.rainfall + (hash % 21) - 10 + coastalBoost + plateauDryness, 0, 150));
  const humidity = Math.round(clamp(base.humidity + seasonal.humidity + (hash % 13) - 6 + Math.round(coastalBoost / 2), 20, 95));
  const temperature = Math.round(clamp(base.temperature + seasonal.temperature + (hash % 9) - 4 - Math.round(coastalBoost / 5), 12, 44));
  const wind = Math.round(clamp(7 + (hash % 18) + rainfall / 18, 4, 32));
  const condition = rainfall > 85 ? "Heavy rain risk" : rainfall > 55 ? "Showers likely" : humidity > 74 ? "Humid and cloudy" : temperature > 34 ? "Hot and dry" : base.condition;

  return {
    temperature,
    humidity,
    rainfall,
    wind,
    condition,
    advisory: rainfall > 85
      ? "Delay spraying and check drainage before irrigation."
      : rainfall < 20
        ? "Plan irrigation and mulch to protect soil moisture."
        : "Weather is suitable for normal field operations.",
  };
}

function fallbackCropProfile(crop) {
  const hash = textHash(crop);
  return {
    base: 1800 + (hash % 90) * 95,
    volatility: 0.09 + (hash % 15) / 100,
    demand: 0.58 + (hash % 28) / 100,
    season: ["Kharif", "Rabi", "Zaid"][hash % 3],
    soil: ["Black soil", "Red soil", "Alluvial soil", "Sandy soil", "Clay soil"][hash % 5],
    waterNeed: ["Low", "Medium", "High"][hash % 3],
  };
}

export function getCropProfile(crop) {
  return crops[crop] ?? fallbackCropProfile(crop);
}

function seasonalScore(input, cropProfile) {
  if (input.season === cropProfile.season) return 1.11;
  if ((input.season === "Kharif" && input.rainfall > 80) || (input.season === "Zaid" && input.temperature > 31)) return 1.03;
  return 0.96;
}

function weatherScore(input, cropProfile) {
  const tempPenalty = Math.abs(input.temperature - 28) * 0.008;
  const humidityBoost = (input.humidity - 55) * 0.002;
  const rainfallBoost = input.rainfall > 55 ? 0.08 : input.rainfall < 18 ? -0.07 : 0.02;
  const waterPenalty = cropProfile.waterNeed === "High" && input.rainfall < 45 ? -0.08 : cropProfile.waterNeed === "Low" && input.rainfall > 95 ? -0.06 : 0;
  return clamp(1 + humidityBoost + rainfallBoost + waterPenalty - tempPenalty, 0.78, 1.2);
}

export function predictPrice(input) {
  const profile = getCropProfile(input.crop);
  const cityScore = cityMultipliers[input.region] ?? (0.94 + (textHash(input.region) % 18) / 100);
  const soilScore = soilFit[input.soil]?.[input.crop] ?? (input.soil === profile.soil ? 1.08 : 0.98);
  const seasonScore = seasonalScore(input, profile);
  const climateScore = weatherScore(input, profile);
  const demandTrend = 1 + profile.demand * 0.09;
  const trendLift = input.forecastDays * profile.volatility * 0.014;

  const linearModel = profile.base * cityScore * soilScore * climateScore;
  const forestModel = profile.base * cityScore * seasonScore * demandTrend;
  const sequenceModel = profile.base * (1 + trendLift) * climateScore * seasonScore;
  const predicted = Math.round((linearModel * 0.32 + forestModel * 0.38 + sequenceModel * 0.3) / 10) * 10;
  const current = Math.round(profile.base * cityScore);
  const change = ((predicted - current) / current) * 100;
  const confidence = Math.round(clamp(88 - profile.volatility * 65 + Math.abs(soilScore - 1) * 20 + (seasonScore > 1 ? 5 : 0), 60, 95));
  const risk = change < -4 || profile.volatility > 0.2 ? "High" : change < 2 ? "Medium" : "Low";
  const demand = profile.demand > 0.77 ? "High" : profile.demand > 0.68 ? "Medium" : "Stable";

  return {
    current,
    predicted,
    change: Number(change.toFixed(1)),
    confidence,
    risk,
    demand,
    sellingWindow: change > 5 ? `Hold ${Math.min(input.forecastDays, 7)} days` : change < -2 ? "Sell soon" : "Watch market",
    cropFit: Math.round(clamp((soilScore * 0.38 + seasonScore * 0.34 + climateScore * 0.28 - 0.82) * 100, 38, 98)),
    models: [
      { name: "Linear Regression", value: Math.round(linearModel) },
      { name: "Random Forest", value: Math.round(forestModel) },
      { name: "LSTM Forecast", value: Math.round(sequenceModel) },
    ],
  };
}

export function buildTrend(crop, region) {
  const profile = getCropProfile(crop);
  const cityScore = cityMultipliers[region] ?? 1;
  return Array.from({ length: 12 }, (_, index) => {
    const wave = Math.sin(index * 0.8) * profile.volatility * 0.8;
    const growth = index * profile.volatility * 0.018;
    return Math.round(profile.base * cityScore * (1 + wave + growth));
  });
}

export function compareCities(crop) {
  const profile = getCropProfile(crop);
  return cities
    .map((city) => ({ city, price: Math.round(profile.base * (cityMultipliers[city] ?? 1) * (1 + profile.volatility * 0.22)) }))
    .sort((a, b) => b.price - a.price);
}

export function recommendCrops(input) {
  return Object.entries(crops)
    .map(([name, profile]) => {
      const soil = soilFit[input.soil]?.[name] ?? (input.soil === profile.soil ? 1.08 : 0.96);
      const season = seasonalScore({ ...input, crop: name }, profile);
      const water = input.rainfall > 70 && profile.waterNeed === "High" ? 1.1 : input.rainfall < 30 && profile.waterNeed === "Low" ? 1.06 : 1;
      const score = Math.round(clamp((soil * 0.42 + season * 0.34 + water * 0.24 - 0.82) * 100, 42, 96));
      return { name, score, reason: `${profile.soil}, ${profile.season} fit` };
    })
    .sort((a, b) => b.score - a.score)
    .slice(0, 5);
}

export function compareCropChoices(input, cropA, cropB) {
  return [cropA, cropB].filter(Boolean).map((crop) => {
    const result = predictPrice({ ...input, crop });
    const profile = getCropProfile(crop);
    const projectedReturn = result.predicted * (Number(input.yieldQty) || 1);
    const score = Math.round(result.cropFit * 0.38 + result.confidence * 0.24 + (result.demand === "High" ? 20 : result.demand === "Medium" ? 14 : 10) + (result.risk === "Low" ? 18 : result.risk === "Medium" ? 10 : 4));
    return {
      crop,
      predicted: result.predicted,
      demand: result.demand,
      risk: result.risk,
      confidence: result.confidence,
      fit: result.cropFit,
      projectedReturn,
      score: clamp(score, 0, 100),
      reason: `${profile.season} season, ${profile.soil}, ${profile.waterNeed.toLowerCase()} water need`,
    };
  }).sort((a, b) => b.score - a.score);
}

export function diagnoseHealth(symptoms, crop = "Crop", imageMeta = null) {
  const selected = new Set(symptoms);
  const imageHint = imageMeta ? ` Photo file "${imageMeta.name}" is attached for agronomist review.` : "";
  if (selected.has("White spots") && selected.has("Yellow leaves")) {
    return { issue: `Possible fungal infection in ${crop}`, fertilizer: "Potassium-rich foliar spray", advice: "Reduce leaf wetness and isolate affected plants.", prevention: `Use morning irrigation and improve airflow.${imageHint}` };
  }
  if (selected.has("Dry soil") && selected.has("Slow growth")) {
    return { issue: `Water stress with nutrient slowdown in ${crop}`, fertilizer: "Balanced NPK compost mix", advice: "Increase watering frequency in small cycles.", prevention: `Mulch soil to reduce evaporation.${imageHint}` };
  }
  if (selected.has("Yellow leaves")) {
    return { issue: `Nitrogen deficiency likely in ${crop}`, fertilizer: "Nitrogen-rich organic manure", advice: "Apply in controlled dose after soil moisture check.", prevention: `Rotate crops and test soil monthly.${imageHint}` };
  }
  return { issue: `No critical issue detected for ${crop}`, fertilizer: "Maintain regular compost schedule", advice: "Monitor leaves and soil moisture weekly.", prevention: `Keep field sanitation and balanced irrigation.${imageHint}` };
}
